class CompiledWeightManager:
    """Manages expert weights with R-PGO precision assignments.

    Each expert's precision was decided at compile time based on its
    activation frequency in the routing profile:
      - HOT experts (freq > 2x uniform): BF16 — full precision, always resident
      - WARM experts (freq > 0.5x uniform): INT8 — good precision, prefetchable
      - COLD experts (freq < 0.1x uniform): INT4 — aggressive compression

    The layout (memory ordering) was determined by Pass A to maximize
    DMA burst efficiency for co-activated experts.
    """

    def __init__(self, quant_assignments: dict[str, str]):
        self.assignments = quant_assignments
        self._resident_experts: set[tuple[int, int]] = set()

        # BF16 experts are always GPU-resident (they're hot)
        for key, prec in quant_assignments.items():
            if prec == "BF16":
                layer, expert = map(int, key.split(":"))
                self._resident_experts.add((layer, expert))

    def get_precision(self, layer_idx: int, expert_idx: int) -> str:
        """Get compile-time precision assignment for an expert."""
        return self.assignments.get(f"{layer_idx}:{expert_idx}", "INT4")

    def is_resident(self, layer_idx: int, expert_idx: int) -> bool:
        """Check if expert is permanently GPU-resident (BF16 hot experts)."""
        return (layer_idx, expert_idx) in self._resident_experts

    def expert_size_bytes(self, layer_idx: int, expert_idx: int) -> int:
        """Get the byte size of an expert at its assigned precision."""
        prec = self.get_precision(layer_idx, expert_idx)
        return BYTES_PER_EXPERT.get(prec, 7_500_000)

