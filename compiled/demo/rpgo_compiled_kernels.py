class PrefetchOrchestrator:
    """Manages the two-stream execution model.

    Stream 0: Expert computation (the actual work)
    Stream 1: Prefetch DMA transfers (overlapped with compute)

    The schedule is fully static — determined at compile time from the
    routing profile. The orchestrator just executes it.
    """

    def __init__(self, n_layers: int):
        self.n_layers = n_layers
        self.compute_stream = torch.cuda.Stream()
        self.prefetch_stream = torch.cuda.Stream()
        self._weight_cache: dict[tuple[int, int], torch.Tensor] = {}
        self._pending_transfers: set[tuple[int, int]] = set()

    def begin_layer(self, layer_idx: int, active_experts: list[int]):
        """Called at the start of each layer's computation.

        Issues prefetches for the NEXT layer based on the static schedule.
        These run concurrently on prefetch_stream while the current layer
        computes on compute_stream.
        """
        for expert_idx in active_experts:
            entries = PREFETCH_TABLE.get((layer_idx, expert_idx), [])
            for entry in entries:
                key = (entry.dst_layer, entry.dst_expert)
                if key not in self._weight_cache and key not in self._pending_transfers:
                    self._pending_transfers.add(key)
                    # Issue async transfer — this is the zero-overhead part.
                    # The decision of WHAT to transfer was made at compile time.
                    # We just execute it.

    def end_layer(self, layer_idx: int):
        """Synchronize prefetch stream before proceeding."""
        self.prefetch_stream.synchronize()
        # Move completed transfers to cache
        self._pending_transfers.clear()

    def get_expert_weight(
        self, layer_idx: int, expert_idx: int
    ) -> Optional[torch.Tensor]:
        """Get a potentially pre-fetched expert weight."""
        return self._weight_cache.get((layer_idx, expert_idx))

